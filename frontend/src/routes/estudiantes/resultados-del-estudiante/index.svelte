                        <p>Los <strong>Resultados del Estudiante</strong> (<strong>RE</strong>) describen lo que se espera que los estudiantes sepan y puedan hacer al momento de la graduación. Estos se relacionan con el conocimiento, las habilidades y los comportamientos que los estudiantes adquieren a medida que avanzan a través del programa.</p>
                        <p>Los Resultados del Estudiante definidos y revisados1 en la Escuela Profesional de Ingeniería de Sistemas son:</p>
                        <p><strong>RE.1</strong>: La capacidad de aplicar conocimientos de matemáticas, ciencias e ingeniería en la identificación, formulación y resolución de problemas complejos de ingeniería.</p>
                        <p><strong>RE.2</strong>: La capacidad de aplicar diseño de ingeniería para producir soluciones a problemas y diseñar sistemas, componentes o procesos para satisfacer necesidades específicas dentro de consideraciones realistas en los aspectos de salud pública, seguridad y bienestar; factores globales, culturales, sociales, económicos y ambientales.</p>
                        <p><strong>RE.3</strong>: La capacidad de comunicarse efectivamente ante audiencias variadas, mediante la comprensión y redacción de informes, así como la realización de exposiciones.</p>
                        <p><strong>RE.4</strong>: La capacidad para reconocer y aplicar principios éticos y asumir responsabilidades profesionales en la práctica de la ingeniería, haciendo juicios informados que consideren el impacto de las soluciones de ingeniería en contextos globales, económicos, ambientales y sociales.</p>
                        <p><strong>RE.5</strong>: La capacidad de desenvolverse eficazmente como miembro o líder en diversos equipos, y en entornos multidisciplinarios, creando un entorno colaborativo e inclusivo, estableciendo metas, planificando apropiadamente tareas y logrando los objetivos planteados.</p>
                        <p><strong>RE.6</strong>: La capacidad de desarrollar y conducir experimentos, análisis, interpretación de datos y síntesis de información para producir conclusiones válidas usando el juicio de ingeniería.</p>
                        <p><strong>RE.7</strong>: El reconocimiento de la necesidad del aprendizaje permanente y la capacidad de adquirir y aplicar nuevos conocimientos usando las estrategias apropiadas, en el más amplio contexto de los cambios tecnológicos.</p>
                        <p><strong>RE.8</strong>: La capacidad de crear, seleccionar y utilizar técnicas, habilidades, recursos y herramientas modernas de ingeniería y tecnologías de la información, incluyendo la predicción y el modelamiento, con una comprensión de las limitaciones.</p>
                        <p><strong>RE.9</strong>: La capacidad de demostrar el conocimiento y comprensión de los principios de gestión en ingeniería y la toma de decisiones, así como su respectiva aplicación.</p>
